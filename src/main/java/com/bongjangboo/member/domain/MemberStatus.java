package com.bongjangboo.member.domain;


public enum MemberStatus {
    /**
     * OAuth 로그인 완료, 온보딩(추가 정보 입력) 미완료
     * - 의미: 소셜 로그인은 성공했지만 서비스 이용을 위한 필수 정보 미입력
     * - 비즈니스 규칙: 온보딩 완료 전까지 메인 서비스 이용 불가
     */
    PENDING_ONBOARDING,

    /**
     * 정상 활동 중인 회원
     * - 의미: 온보딩 완료, 모든 서비스 이용 가능
     * - 비즈니스 규칙: 제한 없이 서비스 이용 가능
     */
    ACTIVE,

    /**
     * 휴면 계정
     * - 의미: 일정 기간(예: 1년) 미접속으로 휴면 전환
     * - 비즈니스 규칙: 휴면 해제 후 서비스 이용 가능, 개인정보 별도 보관
     */
    DORMANT,

    /**
     * 탈퇴한 회원
     * - 의미: 사용자가 탈퇴 요청하여 서비스 이용 종료
     * - 비즈니스 규칙: 개인정보 마스킹, 재가입 제한 기간 적용
     */
    WITHDRAWN,

    /**
     * 관리자에 의해 비활성화된 계정
     * - 의미: 정책 위반, 어뷰징 등으로 관리자가 계정 정지
     * - 비즈니스 규칙: 관리자 승인 전까지 서비스 이용 불가
     */
    INACTIVE,

    SLEEP,
    DEACTIVATED
}
